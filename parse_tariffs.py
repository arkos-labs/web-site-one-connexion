def normalize_city(city):
    return city.upper().replace('’', "'").replace('SAINT', 'SAINT').replace('L\'', 'L').strip()

tariffs = {}

# Parse standard lines
for line in raw_data.strip().split('\n'):
    line = line.strip()
    if not line: continue
    
    # Match pattern: CP CITY N E F VL_N VL_E
    # But CITY can contain spaces and dashes
    # We look for the last 5 numbers
    parts = line.split()
    if len(parts) < 7:
        print(f"Skipping line: {line}")
        continue
        
    prices = parts[-5:]
    try:
        n, e, f, vl_n, vl_e = map(int, prices)
        
        # CP is the first element
        cp = parts[0]
        
        # City is everything between CP and prices
        city_parts = parts[1:-5]
        city = " ".join(city_parts)
        
        # Special handling for some cities that might have been concatenated in the PDF copy-paste
        # e.g. BUSSY-SAINTGEORGES -> BUSSY-SAINT-GEORGES
        # But for now let's keep as is, or try to fix common issues
        
        norm_city = normalize_city(city)
        tariffs[norm_city] = {"N": n, "E": e, "F": f, "VL_N": vl_n, "VL_E": vl_e}
        
    except ValueError:
        print(f"Error parsing prices in line: {line}")

